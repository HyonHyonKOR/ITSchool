package oop20230710;

public class Operator2 {
	
	public static void main(String[] args) {
		// 산술 연산자(Arithmetic Operator)
		//int i = 5;
		//int j = 2;
				
		System.out.println(5 + 2);
		System.out.println(5.0 + 2.0);
		System.out.println(5 - 2);
		System.out.println(5 * 2);
		System.out.println(5 / 2);
		System.out.println(5.0/ 2.0);
		System.out.println(5.0/ 2);   //자동 형변환
		System.out.println(5 % 2);
		
		
	}
}
